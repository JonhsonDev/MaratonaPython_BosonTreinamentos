 # os operadores aritméticos são:
# + soma
# - subtração
# * multiplicação
# / divisão com resultado decimal
# // divisão inteira (descarta as casas decimais)
# % módulo (resto da divisão)
# ** exponenciação (potência)

primeiro_numero = 12
segundo_numero = 10

print('Soma', primeiro_numero + segundo_numero)
print('Subtração', primeiro_numero - segundo_numero)
print('Multiplicação', primeiro_numero * segundo_numero)
print('Divisão com casa decimal', primeiro_numero / segundo_numero)
print('Divisão sem casa decimal', primeiro_numero // segundo_numero)
print('Módulo', primeiro_numero % segundo_numero)
print('Exponenciação', primeiro_numero ** segundo_numero)